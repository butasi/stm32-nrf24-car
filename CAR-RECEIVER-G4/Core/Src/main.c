/* USER CODE BEGIN 4 */
void ProcessControl(ControlPacket *pkt)
{
    int16_t throttle = pkt->pot1 - POT_CENTER;

    if(throttle < DEADZONE && throttle > -DEADZONE)
        throttle = 0;

    if(throttle > 0)
        motor_forward(throttle);
    else if(throttle < 0)
        motor_backward(-throttle);
    else
        motor_stop();

    ProcessButtons(pkt->cmd);
}
void ProcessButtons(uint16_t buttons)
{
    switch(buttons)
    {
        case 1: // right indicator
            HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_14);
            HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_RESET);
            HAL_Delay(300);
            break;

        case 2: // left indicator
            HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);
            HAL_GPIO_WritePin(GPIOB, GPIO_PIN_14, GPIO_PIN_RESET);
            HAL_Delay(300);
            break;

        case 3: // brake light
            HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_15);
            HAL_Delay(300);
            break;

        case 4: //drive
            HAL_Delay(300);
            break;

        case 5:
        	HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_4);
        	HAL_Delay(300);
            break;

        case 6://horn
        	HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_10);
        	HAL_Delay(300);
            break;

        case 7:
            break;

        default:
            break;
    }
}
static void Steering_SetPWM(uint16_t speed, uint8_t dir)
{
    uint32_t arr = __HAL_TIM_GET_AUTORELOAD(&htim2);
    uint32_t duty = (speed * arr) / PWM_MAX;

    if(dir == 1) // left
    {
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, duty);
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, 0);
    }
    else if(dir == 2) // right
    {
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, 0);
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, duty);
    }
    else
    {
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, 0);
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, 0);
    }
}
